package Java;

public class Primitivies {
    // Java primitive data types
    public static void main(String[] args) {
        byte m = 1;
        short smallInt = 128;
        int num = 10;
        char ch = 'k';
        double fractional = 10.99;
        float fractional2 = 1.2f;
        long largeValue = 1009884879;
        System.out.println("Byte : " + m);
        System.out.println("Short value : " + smallInt);
        System.out.println("Integer value: " + num);
        System.out.println("char : " + ch);
        System.out.println("Fractional value floating: " + fractional);
        System.out.println("Fractional value value in double which provides precise value : " + fractional2);
        System.out.println("Large integer value : " + largeValue);
    }
}
